Shendelzare - Vengeful Spirit ( El espiritu vengativo ) 
Historia del Dota (Muy interesante) 
Shendelzare fue una guardiana traicionada y asesinada por Mortred, en quien ella confiaba. 
Con su ultimo suspiro rogó a Elune que le concediera una oportunidad de vengar su muerte. 
A pesar de que su cuerpo murió, su alma incansable se quedó. 
Su sola presencia es un ejemplo para sus aliados mientras sus enemigos temen su determinación. 
A traves de medios del mundo de los espiritus puede atacar con magia pura y cambiar de posicion con otros... 


Zeus - Lord of Olympia (Señor de Olympia) 
Interesante 
Castigado por ayudar al Sentinela en su lucha mortal, Zeus fue desterrado del olimpo y despojado de casi todo su poder. Aun en esta forma debilitada, sus ataques de rayos no son para tomarlos en broma. Tiene hechizos para uno o varios objetivos, lo que lo hace un hechicero especializado. 


Morphling 
Dota 
la Morfocría, la mas magnanima creacion de la naturaleza, la cima de la evolucion, puede transformar su cuerpo liquido para fluir a traves del enemigo haciendole daño, y moviendo a la morfocria. Tambien puede cambiar la composicion de su cuerpo para cambiar fuerza por agilidad y viceversa. 
Ademas ataca a un oponente dañandolo o retardandolo dependiendo de su atributo mas fuerte y su mas sorprendente habilidad, crear una copia de un objetivo obligandolos a enfrentar su propia imagen mientras los aplasta en la confusión. 



Enchantress 
historia 
Aunque todas las Driades son hijas de Cenarius, Aiutsha es su primogénita y su favorita. Por est a relacion tan directa, ella toma prestados muchos de sus poderes, como el conrol de las hadas que curan a los aliados, ademas, prefiriendo el combate de rango que el de mele, consiguio una lanza que ataca con mas fuerza cuanto mas lejos esta el objetivo. Su agilidad es incomparable y cualquiera que le ataque vera cuan facilmente ella escapa. 



Slithice - Naga Siren 
Heroes 
Slithice siempre fue una vagabunda entre los naga, vagando por los limites del mar en busca de nuevas cosas. Durante sus aventuras ella conocio y se enamoró de un joven orco que estaba entrenando para ser un Maestro de las Espadas. A pesar de que este orco era aun inexperimentado, le enseñó varias tácticas orcas de batalla empleadas por guerreros experimentados. Durante su cortejo, y mientras estaba aprendiendo a hacer trampas de un cazador local, una plaga del Scourge vino e invadió, su amor fue asesinado enfrente de sus propios ojos. Ella uso sus magias naga para ponerlos a todos a dormir y huyó. Ahora se entrega toda al Sentinel, en el nombre de su amor. 



Jah'rakal - Troll Warlord 
Historia del Dota (Muy interesante) 
Cuando el Sentinel estuvo reclutando sus fuerzas, los guereros trolls fueron considerados primitivos y no dignos de confianza. Su orgullo fue herido, y rehusaron unirse. Algunos incluso consideraron afiliarse al Scourge. Solo un joven Señor de la Guerra llamado Jah'rakal se unió al Sentinel. Conocido como "Hacha Cegadora" en lengua troll por su cegadora velocidad al usar las hachas y su tecnica cegadora, sus furias incontrolables ya lo han hecho una leyenda entre sus aliados. Como sea, Jah'rakal no pelea por el Sentinel, ni siquiera para porteger a los Ancestros. El pelea para demostrarle a los elfos, orcos, humanos y cualquier otro ser que los guerreros trolls son los mejores del mundo. 


Darkterror - Faceless Void 
Interesante 
Dicen que alguna vez fue humano, pero su pasado esta sumergido en la oscuridad, incluso para el mismo. Lo que se sabe es que fue arrojado entre el Vacío existente entre los mundos y que emergió con el poder de manipular el tiempo. El puede congelar a sus enemigos en el tiempo y evadir ataques viajando partes de segundo atrás en el tiempo, justo antes de recibir el golpe. El puede crear brevemente una rasgadura en la fábrica de espacio-tiempo, capturando en ella enemigos y aliados por igual, pero manteniéndose el mismo libre de aquel efecto. Se rumorea que puede matar cualquier hombre en una legion de soldados, pero nadie lo ve moverse... 


Strygwyr - Bloodseeker 
Dota 
Historia: Mitad demonio, mitad orco. Strygwyr es un desterrado de todas las formas de vida. 
Aceptado por el Scourge, el usa su habilidad para controlar la vida de sus enemigos, dándoles muerte a toda costa. 
Es un ser sediento de sangre, q adora frenéticamente ver morir a sus enemigos y termina descuartizándolos con una carcajada de feroz alegría. 



N`aix - The Lifestealer 
historia 
Habia una vez un hombre en Lordaeron tan consumido por la avaricia que estuvo fuera del remordimiento. La tentativa eventualmente cogida de robar el sagrado anillo del Principe Arthas, fue intentado y ahorcado. Ain su cuerpo, tan corrompido por su deseo de robar, para tenido elevado por si mismo en forma de a un demonio monstruoso. Entonces nacio N'aix, su viejo nombre mucho tiempo olvidado: la encarnaci?n de lujuria y avaricia, doblada en el robo de la vida de cada criatura viva que el encuentra. 


Raigor Stonehoof - The Earthshaker 
Heroes 
Ingreso al Sentinel para defender el Arbol del Mundo, el origen de todas las cosas naturales en el mundo. Un adepto terrestre, Raigor se especializo en castear conjuros de rango corto que golpearan duro en un area. El ademas puede golpear muy duro con su totem. 


Barathrum - Spiritbreaker ( Rompe espiritus ) 
Historia del Dota (Muy interesante) 
Baratrum era un excluido del clan de los Spirithoof, asi que se escapo en medio de la verguenza al plano etéreo, alimentando espiritus errantes para que le sostuvieran. Vago asi, sin rumbo por siglos. Encontrado y reclutado por el rey Lich, la velocidad imposible y poderes casi divinos de barathrum han sido liberados en el plano material. 
Su linterna capaz de proyectar energias etereas lanza a sus oponentes hacia atras, con una fuerza repentina. Con la habilidad de acortar distancias entre el y su presa es mas que un oponente digno para cualquiera. 



Crixalix - Sand King ( Rey de las arenas ) 
Interesante 
en las planicies de Kalimdor, nacio crixalix como un escorpion normal. por razones desconocidas creció mucho mas que sus hermanos. Sujeto al control mental del Rey Lich, fue convocado a defender el trono helado. 
A pesar de su descomunal tamaño no ha perdido sus poderes de escorpion, tales como enterrarse en la arena y surgir en cualquier sitio, empalando a todo lo que toque su despertar. Tambien posee un veneno toxico que causa a sus presas explotar, y los exploradores han descubierto que es capaz de crear vibraciones monstruosas en la arena, capaces de acabar con cualquiera tan estupido como para acercarse. 



Black Arachnia - The Broodmother (madre de la camada) 
Dota 
Una vez reina de las Nerubian, inspiraba el temor en los corazones de los que comandaba. Cometio el fatal error de enamorarse de el asesino renegado Anub'arak. Por esto, escapo de su reino para luchar junto a su amado llevando en su vientre su descendencia como simbolo de su amor. 
una maestra en tejer redes de engaño, incapacita a quien se atreve a atacarla tomando venganza al injertarle la semilla de si vientre, cuyas crias se alimentan del cuerpo destrozandolo desde adentro. Su hambre por la batalla le hace crecer y se rumora que su hambre por la carne es insaciable... 



Rotund`jere - The Necrolyte (Necrolito) 
historia 
Castigado por pecados de herejia por hombres que se llamaba a si mismos santos, la muerte solo le dio a rotund'jere una ira incontrolable con los vivos, la cual expresa enviando espiritus malignos a sus enemigos. Siempre que un ser viviente muere por su mano, se llena de placer y crece en poder, su misma presencia hace que las cosas se pudran y deshagan. Un maestro de la tortura y el dolo aprovecha la debilidad y cercena las vidas sin piedad, siempre con una sonrisa horrible en su muerto rostro. 


Rexxar - The Beastmaster (Maestro de las Bestias) 
Heroes 
Un vago sin rumbo de los Mock'nathal,(los miticos mitad orco, mitad ogro), rexxar y sus hachas se han unido al Sentinel para proteger mejor el mundo natural.Amigo de las bestias, Ataca con violencia y salvajismo incontrolable llegando a arrojar sus hachas cual bumerans ara llegar a la victoria. Es un aliado para contar cuando se le necesita más. 



Rhasta - Shadow Shaman 
Historia del Dota (Muy interesante) 
Una vez, a un poderoso brujo llamado Rhasta, se le fueron enseñados los poderes de la magia voodoo. Cuando la guerra entre Sentinels y Scourges comenzó, Rhasta, entonces un cazador de sombras, ofrecio su lealtad a los Sentinels para vencer a los Scourges de una vez y para siempre. Al ser un poderoso brujo, Rhasta se pudo convertir en un poderoso convocador de totem, serpientes, inutilizador de enemigos,y tambien aprendio a dominar los poderes del rayo para severas unidades. 


Meepo- Geomancer 
Interesante 
Originalmente adorado por las tribus paganas de Germania y de los bosques negros que lo abarcaron. Meepo es un pequeño espíritu travieso de la tierra que goza enterrar a sus enemigos vivos en los picos de las montañas de roca, sujetandolos en su impotencia mientras el los aporrea en sumisión con ayuda de su poderosa pala. La mayor inquietud de los poderes del Geomancer sinembargo es su habilidad de separar su ser en multiples clones de el mismo, cada uno tan poderoso como el original y haciendolo potencialmente por 4 veces el problema para el desafortunado que lo encuentre. 


Rikimaru - The Stealth Assassin 
Dota 
Nacido como el heredero del dominio del sátiro, Rikimaru fue entrenado por los guerreros más poderosos de su raza. Sin embargo, el poder de la Legion del Fuego(Burning Legion) logró corromper a muchos de su clase, abandonandolos en el bosque como bestias estupidas y sin inteligencia. Prometiendo la venganza contra el Azote(Scourge), agudizo sus habilidades para la batalla. Utilizando su pequeño tamaño como ventaja, el puede hacerse invisible permitiendose apuñalar a sus enemigos por la espalda. Y utilizando un denso humo como cubierta llega a ser casi intocable. 




Mortred - Phantom Assasin ( Fantasma asesino ) 
historia 
Nacida en la orden de los elfos, reclusa de la noche, reconocida por los guardianes y evitada por la sociedad de los elfos de la noche. Mortred prometio lealtad a The Scourge para tomar venganza. 
Mortred es una mezcla perfecta entre la fuerza y la velocidad, paso los años sola en el bosque de Ashenvale, hasta que le permitieron mezclarse en el terreno, apareciendo como un fantasma, de vez en cuando y golpeando cuando menos se la esperaba. La precisión mortal con la cual ella lleva acabo sus ataques la ha hecho un miembro valido en The Scourge y es claro por qué la conocen como el fantasma asesino. 



Boush - Tinker (Pensador) 
Heroes 
Boush fue educado en tecnologia goblin de avanzada, haciendolo capaz de atacar a sus enemigos desde la distancia y manteniendose a salvo. Tambien puede mejorar sus dispositivos para poder usarlos mas rapidamente. Una verdadera demostracion del poder del conocimiento. 



Leviathan - Tidehunter 
Historia del Dota (Muy interesante) 
El más grande de los gigantes de mar, Leviathan fue sellado lejos en consecuencia de la guerra entre los antiguos dioses y los Titanes en tiempos de los ancestros. Ahora, liberado por el Rey Lich para que le ayude en su lucha contra el Sentinel, él divide las olas frente a sus enemigos con su poderosa ancla, despedazando asedios enemigos incluso antes que ellos comiencen. Es un enemigo tan terrible en la tierra como en el agua, aquéllos que están de pie antes que el Tidehunter son barridos en un mar de púas, estrujándose cuando él los encierra en su pulverización a los que se van para recibir su ira. 


Lycantrophe - Banehallow 
Interesante 
Banehallow es un avatar de furia bestial creado por el Scourge. Por años él ha habitado en los bosques oscuros del mundo, cazando inocentes que vaguen en el. Ahora él esta convocado para ayudar a sus amos. Banehallow se revela en sangre y en la batalla, usando su temple de lobo, atrayendo bestias salvajes a su lado e imbuyendo a sus aliados con su fuerza, hace trizas la carne. También es increíblemente rápido y ágil. Incluso se dice que puede transformarse en un lobo gigante, dándose a sí mismo tanta resistencia y velocidad como para acalanar y mutilar a todo aquel que esté cerca. 



Lesale Deathbringer - Venomancer 
Dota 
Para aquellos obsesionados con la muerte, el veneno es un estudio necesario. Sin embargo, Lesale fue un paso demasiado lejos. El experimentó tanto en él mismo con varios tóxicos y químicos que logró aprender los secretos de la muerte. Estos retorcieron su mente y deformaron su cuerpo en una criatura horrorosa que encarna un inmenso odio por todas las cosas puras. El puede golpear a un enemigo con un tiro concentrado de veneno o incluso puede extender un gas tóxico a lo largo de un área entera, muchas veces debilitando a bestias y humanos por igual. 



Balanar - The Night Stalker 
historia 
Balanar fue uno de los élite Dreadlords antes de caer en la Legión de Fuego (Burning Legion). Ahora que la Legión de Fuego se fue, vive su vida atacando pueblos y emboscando a sus enemigos mas odiados -Los Elfos de la noche (Night Elves)-. El utiliza su poder de la luna, así se vuelve mas fuerte durante la noche. Balanar tambien puede utilizar su control con el tiempo para cambiar el dia a noche que refuerza aún más su poder. Su presencia inspira el temor en el corazón de muchos. 



Traxex - The Drow Ranger 
Heroes 
Traxex fue un arquera muy hábil en su furtiva y tiranica patria: Underdark, hasta que crecio enferma por sus males de familia que la obligaron a abandonar la superficie terrestre. Se unió al lado Centinela (Sentinel) para mejorar sus habilidades, y aunque todavía la tratan con desconfianza por sus orígenes, ella se ha probado a si misma repetidas veces. Traxex es una extraordinaria cazadora, persiguiendo a sus enemigos con sus flechas de hielo y deshabilitando a los pocos magos que osan enfrentarla. 
La raza Drow es una mistica raza de elfos marcados con cabello claro y piel oscura. Los Drow son conocidos por hacer sus casas debajo de la tierra, la ciudad de UnderDark, donde veneran a la diosa del caos, Lloth. 



Aggron Stonebreaker - The Ogre Magi 
Historia del Dota (Muy interesante) 
El campeón de los ogros de Stoneblade(Espada de Piedra), Aggron es uno de los héroes más fuertes y más listos del lado Centinela(Sentinel). Su especialidad es lanzar sus hechizos multiples veces, como su magia de daño y soporte que resulta excepcionalmente poderosa. Apoya al lado Centinela para defender su propia tierra. El es un héroe de apoyo muy valorado en los Centinela. 




MogulKhan-Axe 
Interesante 
En la guerra entre humanos y orcos que ocurrio hace mucho tiempo, los guerreros orcos fueron corrompidos por influencias demoniacas y convertidos en herramientas de la oscuridad. Uno de esos guerreros era el lider de la Mitad - Tribu de las Hachas Sangrientas; Mogul Khan. Mogul due destruido por los Paladines humanos pero misteriosamente ha sido visto peleando por el Scourge alrededor del tiempo que Mannoroth comenzo a comandar a los orcos en la guerra contra Archimonde. Los Hachas han sido revividos por demonios para usar sus reflexiones y poderosos ataques para cazar y encontrar a los agiles guerreros del Sentinel y cortarlos de lado a lado e incluso su lujo por la guerra en ellos 




Slardar-SlithereenGuard 
Dota 
El poderoso rey de la raza Naga, Slardar fue despertado de las profundidades del mar despues de la invasion de la legión de fuego. Encontrando el mundo consumido por el caos el encontro la oportunidad perfecta para derrotar a los odiados elfos nocturnos. Aliandose con el Azote, Slardar usa su tremenda fuerza y poder para paralizar y quebrantar a sus enemigos y su poderoso tridente puede perforar la más poderosa armadura con facilidad 




Jakiro - Twin Head Dragon 
historia 
Originalmente fue encontrado carcomiendo las raíces del Arbol del Mundo durante una excavación, la magnifica edad de las Quimeras es imposible de determinar. Con sus colosales cabezas rápidas y agresivas, comportamiento impredecible, decir que Jakiro es una criatura intimidante es realmente insuficiente, aun es leal al Sentinel por sus propias razones. Debido a su naturaleza mística, posee una extraña afinidad hacia las fuerzas elementales de hielo y fuego. Ello le permite devastar todo a su paso, es muy capaz de mantener a sus oponentes en su línea de furia. El Scourge haría bien en guardarse de este dragón, para no congelarse ni despedazarse como su precioso Trono de Hielo ante sus ojos. 



Knight Davion - Dragon Knight 
Heroes 
